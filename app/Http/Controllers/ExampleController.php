    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $example = Examples::findOrFail($id);

        return view('edit', compact('example'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $validation = $request->validate(['name'=>'required|max:255',
            'email'=>'required|email']);
        Examples::whereId($id)->update($validation);

        return redirect('/example')->with('success', 'Updated successfully!');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $post = Examples::findOrFail($id);
        $post->delete();
        return redirect('/example')->with('success', 'Deleted successfully');

    }

